/**
 * Generate KICKOFF.md for the output package.
 *
 * @param {string} goal
 * @param {object} bdiTrace
 * @param {object} ranking
 * @returns {string}
 */
function generateKickoffMd(goal, bdiTrace, ranking) {
  const topSkills = ranking.ranked.slice(0, 12);

  const skillBlock = topSkills
    .map(s =>
      `  - slug: ${s.slug}\n    role: ${s.role}\n    reason: >\n      ${s.causalChain.slice(0, 200).replace(/\n/g, ' ')}`
    )
    .join('\n\n');

  const executionSteps = topSkills.map(
    (s, i) => `${i + 1}. **${s.name || s.slug}** (\`${s.slug}\`) — ${s.role}`
  );

  const expectedOutput = bdiTrace.desires
    .map(d => `- ${d.content}`)
    .join('\n');

  return `# Project: ${goal}

## What you're building

${goal}

## Your skills

\`\`\`yaml
skills:
${skillBlock || '  # (no skills selected)'}
\`\`\`

## Execution plan

${executionSteps.join('\n')}

## Expected output

${expectedOutput}

---

*Generated by WorkspaceBuildR — BDI cognitive workspace assembler*
*Model: ${ranking.modelUsed} | Offline: ${ranking.offline}*
`;
}

/**
 * Generate CLAUDE.md for the output package.
 *
 * @param {string} goal
 * @param {object} ranking
 * @returns {string}
 */
function generateClaudeMd(goal, ranking) {
  const core = ranking.ranked.filter(s => s.role === 'core');
  const supporting = ranking.ranked.filter(s => s.role === 'supporting');
  const optional = ranking.ranked.filter(s => s.role === 'optional');

  const fmt = (skills) =>
    skills.length === 0
      ? '  # (none)'
      : skills
          .map(
            s =>
              `  - slug: ${s.slug}\n    reason: >\n      ${s.causalChain.slice(0, 300).replace(/\n/g, ' ')}`
          )
          .join('\n\n');

  const rejectedRows =
    ranking.rejected.length > 0
      ? '\n## Rejected Skills\n\n| Skill | Reason |\n|-------|--------|\n' +
        ranking.rejected
          .map(r => `| \`${r.slug}\` | ${r.reason.slice(0, 130).replace(/\|/g, '/')} |`)
          .join('\n')
      : '';

  return `# Skills for: ${goal}

## Core Skills

\`\`\`yaml
skills:
${fmt(core)}
\`\`\`

## Supporting Skills

\`\`\`yaml
skills:
${fmt(supporting)}
\`\`\`

## Optional Skills

\`\`\`yaml
skills:
${fmt(optional)}
\`\`\`
${rejectedRows}

---

*Generated by WorkspaceBuildR | Model: ${ranking.modelUsed} | Offline: ${ranking.offline}*
`;
}

/**
 * Generate REASONING.md for the output package.
 *
 * @param {string} goal
 * @param {object} bdiTrace
 * @param {object} ranking
 * @returns {string}
 */
function generateReasoningMd(goal, bdiTrace, ranking) {
  const beliefsSection = bdiTrace.beliefs
    .map(
      b =>
        `### ${b.content}\n\n` +
        `**World state:** \`${JSON.stringify(b.worldState).slice(0, 250)}\`\n\n` +
        `**Justified by:** ${b.justification}`
    )
    .join('\n\n');

  const desiresSection = bdiTrace.desires
    .map(d => `- **${d.content}**\n  Motivated by beliefs: ${d.motivatedBy.join(', ')}`)
    .join('\n\n');

  const intentionsSection =
    ranking.ranked.length === 0
      ? '_No skills selected._'
      : ranking.ranked
          .map(
            s =>
              `### \`${s.slug}\` — ${s.role.toUpperCase()} (score: ${s.score}/100)\n\n${s.causalChain}`
          )
          .join('\n\n');

  const rejectedSection =
    ranking.rejected.length === 0
      ? '_No skills rejected._'
      : ranking.rejected.map(r => `- \`${r.slug}\`: ${r.reason}`).join('\n');

  const coreCount = ranking.ranked.filter(s => s.role === 'core').length;
  const totalSelected = ranking.ranked.length;
  const confidence =
    totalSelected > 0 ? Math.round((coreCount / totalSelected) * 100) : 0;

  const offlineNote =
    ranking.offline
      ? '\n> **Note:** Generated in offline mode. Causal chains are token-overlap approximations. Set `GROQ_API_KEY` for full LLM reasoning.\n'
      : '';

  return `# Reasoning Trace

Generated: ${new Date().toISOString()}
Model: ${ranking.modelUsed}
Offline: ${ranking.offline}

---

## Goal

> ${goal}

---

## Beliefs

${beliefsSection}

---

## Desires

${desiresSection}

---

## Intentions (selected)

${intentionsSection}

---

## Rejected skills (with reasons)

${rejectedSection}

---

## Confidence: ${confidence}/100

Confidence is the ratio of core-role skills to total selected skills.
A high ratio indicates strong causal alignment between selected skills and goal requirements.
${offlineNote}`;
}

/**
 * Generate workflows/main.yaml for the output package.
 *
 * @param {string} goal
 * @param {object} bdiTrace
 * @param {object} ranking
 * @returns {string}
 */
function generateWorkflowYaml(goal, bdiTrace, ranking) {
  const steps = ranking.ranked
    .map((s, i) => {
      const safeId = s.slug.replace(/[^a-z0-9]/gi, '_').toLowerCase().slice(-40);
      return (
        `  - id: step_${String(i + 1).padStart(2, '0')}_${safeId}\n` +
        `    name: "${(s.name || s.slug).replace(/"/g, "''")}"\n` +
        `    skill: "${s.slug}"\n` +
        `    role: ${s.role}\n` +
        `    score: ${s.score}\n` +
        `    causal_justification: >\n` +
        `      ${s.causalChain.slice(0, 250).replace(/\n/g, ' ')}`
      );
    })
    .join('\n\n');

  const desiredOutcomes = bdiTrace.desires
    .map(d => `  - "${d.content.replace(/"/g, "'")}"`)
    .join('\n');

  return `# WorkspaceBuildR generated workflow
# Goal: ${goal}
# Generated: ${new Date().toISOString()}
# Model: ${ranking.modelUsed}

name: workspace-workflow
goal: "${goal.replace(/"/g, "'")}"

desired_outcomes:
${desiredOutcomes}

steps:
${steps || '  # (no steps — no skills were selected)'}

metadata:
  offline: ${ranking.offline}
  total_skills: ${ranking.ranked.length}
  core_skills: ${ranking.ranked.filter(s => s.role === 'core').length}
  supporting_skills: ${ranking.ranked.filter(s => s.role === 'supporting').length}
  optional_skills: ${ranking.ranked.filter(s => s.role === 'optional').length}
  rejected_skills: ${ranking.rejected.length}
`;
}

