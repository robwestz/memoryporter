/**
 * Score a single test case JSON file.
 *
 * Scoring breakdown:
 *   40 pts — expected skills present in selected list
 *   40 pts — causal expectations matched in causal chains
 *   20 pts — not-expected skills correctly rejected
 *
 * @param {string} testCasePath
 * @param {object[]} catalog
 * @param {object} llmClient
 * @returns {Promise<{ file: string, goal: string, score: number, details: string[] }>}
 */
async function runTestCase(testCasePath, catalog, llmClient) {
  const raw = await readFile(testCasePath, 'utf-8');
  const tc = JSON.parse(raw);
  const {
    goal,
    expectedSkills = [],
    causalExpectations = [],
    notExpected = [],
  } = tc;

  const { ranking } = await assemble(goal, catalog, llmClient);
  const selectedSlugs = new Set(ranking.ranked.map(s => s.slug));
  const rejectedSlugs = new Set(ranking.rejected.map(s => s.slug));

  const details = [];
  let points = 0;
  let maxPoints = 0;

  // --- Part 1: Expected skills present (40 pts) ---
  if (expectedSkills.length > 0) {
    const pts = 40 / expectedSkills.length;
    for (const slug of expectedSkills) {
      maxPoints += pts;
      if (selectedSlugs.has(slug)) {
        points += pts;
      } else {
        details.push(`MISS: "${slug}" expected in selected but absent`);
      }
    }
  }

  // --- Part 2: Causal chain quality (40 pts) ---
  if (causalExpectations.length > 0) {
    const pts = 40 / causalExpectations.length;
    for (const exp of causalExpectations) {
      maxPoints += pts;
      const ranked = ranking.ranked.find(s => s.slug === exp.skill);
      if (!ranked) {
        details.push(`MISS: causal expectation for "${exp.skill}" but skill not selected`);
        continue;
      }
      const expectedWords = exp.causation
        .toLowerCase()
        .split(/\W+/)
        .filter(w => w.length > 4);
      const chainLower = (ranked.causalChain || '').toLowerCase();
      const matched = expectedWords.filter(w => chainLower.includes(w)).length;
      const ratio = expectedWords.length > 0 ? matched / expectedWords.length : 0;
      if (ratio >= 0.4) {
        points += pts;
      } else {
        details.push(
          `WEAK CAUSATION: "${exp.skill}" chain matched ${Math.round(ratio * 100)}% of expected terms`
        );
      }
    }
  }

  // --- Part 3: Not-expected correctly rejected (20 pts) ---
  if (notExpected.length > 0) {
    const pts = 20 / notExpected.length;
    for (const slug of notExpected) {
      maxPoints += pts;
      if (rejectedSlugs.has(slug)) {
        points += pts;
      } else if (selectedSlugs.has(slug)) {
        details.push(`FALSE POSITIVE: "${slug}" should be rejected but was selected`);
      } else {
        // Not addressed at all — partial credit
        points += pts * 0.5;
        details.push(`OMISSION: "${slug}" should be explicitly rejected but not mentioned`);
      }
    }
  }

  const score = maxPoints > 0 ? Math.round((points / maxPoints) * 100) : 0;
  return { file: testCasePath, goal, score, details };
}

// ---------------------------------------------------------------------------
// CLI entry point
// ---------------------------------------------------------------------------

async function main() {
  const args = process.argv.slice(2);

  if (args.length === 0 || args.includes('--help')) {
    console.log('WorkspaceBuildR — BDI Workspace Assembler\n');
    console.log('Usage:');
    console.log('  node agent/assembler.mjs "your goal here"');
    console.log('  node agent/assembler.mjs --offline "your goal"');
    console.log('  node agent/assembler.mjs --test test-cases/rest-api.json');
    console.log('  node agent/assembler.mjs --test-all');
    console.log('\nEnvironment:');
    console.log('  GROQ_API_KEY   Groq API key (free at console.groq.com)');
    console.log('                 Omit to run in offline mode automatically');
    process.exit(0);
  }

  const isOffline = args.includes('--offline');
  const isTest = args.includes('--test');
  const isTestAll = args.includes('--test-all');

  const catalog = await loadCatalog();
  const llmClient = createClient(isOffline ? { provider: 'offline' } : {});

  // --test-all
  if (isTestAll) {
    const testDir = join(REPO_ROOT, 'test-cases');
    let testFiles = [];
    try {
      const entries = await readdir(testDir);
      testFiles = entries.filter(f => f.endsWith('.json')).map(f => join(testDir, f));
    } catch {
      console.error('[test-all] Cannot read test-cases/ directory.');
      process.exit(1);
    }

    if (testFiles.length === 0) {
      console.log('[test-all] No .json files found in test-cases/');
      process.exit(0);
    }

    console.log(`\n[test-all] Running ${testFiles.length} test cases...\n`);
    const results = [];

    for (const tf of testFiles) {
      try {
        const result = await runTestCase(tf, catalog, llmClient);
        results.push(result);
      } catch (err) {
        results.push({
          file: tf,
          goal: '(error)',
          score: 0,
          details: [`ERROR: ${err.message}`],
        });
      }
    }

    const W = 32;
    console.log('\n' + '='.repeat(72));
    console.log('TEST RESULTS — WorkspaceBuildR');
    console.log('='.repeat(72));
    console.log(`${'Test case'.padEnd(W)} ${'Score'.padStart(6)}  Bar`);
    console.log('-'.repeat(72));
    for (const r of results) {
      const name = r.file.split(/[/\\]/).pop().replace('.json', '');
      const bar = '█'.repeat(Math.round(r.score / 10)) + '░'.repeat(10 - Math.round(r.score / 10));
      console.log(`${name.padEnd(W)} ${String(r.score).padStart(5)}%  ${bar}`);
      for (const d of r.details) {
        console.log(`  ${d}`);
      }
    }
    console.log('='.repeat(72));
    const avg = Math.round(results.reduce((s, r) => s + r.score, 0) / results.length);
    console.log(`Average: ${avg}/100`);
    console.log('='.repeat(72) + '\n');
    return;
  }

  // --test <file>
  if (isTest) {
    const testFileArg = args.find(a => !a.startsWith('--'));
    if (!testFileArg) {
      console.error('[test] Usage: node agent/assembler.mjs --test test-cases/rest-api.json');
      process.exit(1);
    }
    const testPath =
      testFileArg.startsWith('/') || /^[A-Za-z]:/.test(testFileArg)
        ? testFileArg
        : join(process.cwd(), testFileArg);

    try {
      const result = await runTestCase(testPath, catalog, llmClient);
      console.log('\n' + '='.repeat(60));
      console.log(`TEST: ${result.file.split(/[/\\]/).pop()}`);
      console.log(`Goal: ${result.goal}`);
      console.log(`Score: ${result.score}/100`);
      if (result.details.length > 0) {
        console.log('\nDivergences:');
        for (const d of result.details) console.log(`  - ${d}`);
      } else {
        console.log('All expectations met — no divergences.');
      }
      console.log('='.repeat(60) + '\n');
    } catch (err) {
      console.error(`[test] Failed: ${err.message}`);
      if (process.env.DEBUG) console.error(err.stack);
      process.exit(1);
    }
    return;
  }

  // Normal assembly run
  const goal = args.filter(a => !a.startsWith('--')).join(' ').trim();
  if (!goal) {
    console.error('[assembler] No goal provided. Run with --help for usage.');
    process.exit(1);
  }

  try {
    const { outputDir, ranking } = await assemble(goal, catalog, llmClient);

    const coreCount = ranking.ranked.filter(s => s.role === 'core').length;
    const supportCount = ranking.ranked.filter(s => s.role === 'supporting').length;
    const optCount = ranking.ranked.filter(s => s.role === 'optional').length;

    console.log('\n' + '='.repeat(60));
    console.log('ASSEMBLY COMPLETE');
    console.log('='.repeat(60));
    console.log(`Goal:    ${goal}`);
    console.log(`Output:  ${outputDir}`);
    console.log(`Skills:  ${ranking.ranked.length} selected (${coreCount} core, ${supportCount} supporting, ${optCount} optional)`);
    console.log(`         ${ranking.rejected.length} rejected with documented reasons`);
    console.log(`Model:   ${ranking.modelUsed}`);
    console.log('='.repeat(60));
    console.log('\nPackage files:');
    console.log(`  ${outputDir}/KICKOFF.md`);
    console.log(`  ${outputDir}/CLAUDE.md`);
    console.log(`  ${outputDir}/REASONING.md`);
    console.log(`  ${outputDir}/workflows/main.yaml`);
    console.log('');
  } catch (err) {
    console.error(`[assembler] Fatal error: ${err.message}`);
    if (process.env.DEBUG) console.error(err.stack);
    process.exit(1);
  }
}

main();
