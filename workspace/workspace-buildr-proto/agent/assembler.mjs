/**
 * Run the full assembly pipeline for a goal.
 *
 * @param {string} goal
 * @param {object[]} catalog
 * @param {object} llmClient
 * @returns {Promise<{ outputDir: string, bdiTrace: object, ranking: object }>}
 */
async function assemble(goal, catalog, llmClient) {
  console.log(`\n[assembler] Goal: "${goal}"`);
  console.log(`[assembler] Provider: ${llmClient.provider} | Model: ${llmClient.model}`);

  // Step 1: BDI deliberation (offline, causal, no LLM)
  console.log('[assembler] Running BDI deliberation...');
  const bdiTrace = deliberate(goal, catalog);
  console.log(
    `[assembler] Beliefs: ${bdiTrace.beliefs.length} | ` +
    `Desires: ${bdiTrace.desires.length} | ` +
    `Pre-selected: ${bdiTrace.intentions.selected.length} | ` +
    `Pre-rejected: ${bdiTrace.intentions.rejected.length}`
  );

  // Step 2: LLM ranking (or offline fallback)
  console.log('[assembler] Ranking skills with causal analysis...');
  const ranking = await rankSkills(goal, catalog, llmClient);
  console.log(
    `[assembler] Ranked: ${ranking.ranked.length} | ` +
    `Rejected: ${ranking.rejected.length} | ` +
    `Offline: ${ranking.offline}`
  );

  // Step 3: Write output package
  const slug = slugify(goal);
  const outputDir = join(REPO_ROOT, 'outputs', slug);
  const workflowDir = join(outputDir, 'workflows');

  await mkdir(workflowDir, { recursive: true });

  await writeFile(join(outputDir, 'KICKOFF.md'), generateKickoffMd(goal, bdiTrace, ranking), 'utf-8');
  await writeFile(join(outputDir, 'CLAUDE.md'), generateClaudeMd(goal, ranking), 'utf-8');
  await writeFile(join(outputDir, 'REASONING.md'), generateReasoningMd(goal, bdiTrace, ranking), 'utf-8');
  await writeFile(join(workflowDir, 'main.yaml'), generateWorkflowYaml(goal, bdiTrace, ranking), 'utf-8');

  console.log(`[assembler] Package written: ${outputDir}`);
  return { outputDir, bdiTrace, ranking };
}

