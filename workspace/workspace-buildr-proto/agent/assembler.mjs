import { readFile, writeFile, mkdir, readdir } from 'fs/promises';
import { existsSync } from 'fs';
import { fileURLToPath } from 'url';
import { join, dirname } from 'path';

import { deliberate, DEFAULT_CATALOG } from './bdi.mjs';
import { createClient } from './llm.mjs';
import { rankSkills } from './ranker.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..');

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/**
 * Slugify a goal string into a safe directory name.
 *
 * @param {string} goal
 * @returns {string}
 */
function slugify(goal) {
  return goal
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
    .slice(0, 80);
}

/**
 * Load the skill catalog.
 * Tries data.json at repo root first; falls back to DEFAULT_CATALOG.
 *
 * @returns {Promise<object[]>}
 */
async function loadCatalog() {
  const dataPath = join(REPO_ROOT, 'data.json');
  if (existsSync(dataPath)) {
    try {
      const raw = await readFile(dataPath, 'utf-8');
      const parsed = JSON.parse(raw);
      const catalog = Array.isArray(parsed)
        ? parsed
        : parsed.catalog || parsed.skills || [];
      if (catalog.length > 0) {
        console.log(`[catalog] Loaded ${catalog.length} skills from data.json`);
        return catalog;
      }
    } catch (err) {
      console.warn(`[catalog] Failed to parse data.json (${err.message}), using built-in catalog.`);
    }
  }
  console.log(`[catalog] Using built-in catalog (${DEFAULT_CATALOG.length} skills)`);
  return DEFAULT_CATALOG;
}

